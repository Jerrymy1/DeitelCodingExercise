package chapterThree;

public class Native {
    private String name;
    private String phoneNumber;

    public void setName(String name){
        this.name = name;
    }
    public void setPhoneNumber(String phoneNumber){
        this.phoneNumber = phoneNumber;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }
    public String getName(){
        return name;
    }

}
